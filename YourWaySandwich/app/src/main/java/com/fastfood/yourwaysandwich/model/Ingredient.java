package com.fastfood.yourwaysandwich.model;

public class Ingredient {
    public int  id;
    public String name;
    public float price;
    public String image;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public float getPrice() {
        return price;
    }

    public String getImageUrl() {
        return image;
    }
}
